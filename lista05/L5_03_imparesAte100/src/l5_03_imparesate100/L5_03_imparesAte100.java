
package l5_03_imparesate100;


public class L5_03_imparesAte100 {

    public static void main(String[] args) {
       // 3) Faça um programa que mostre na tela os números ímpares de 1 a 100. 
        
        System.out.println("numeros impares de 1 a 100: ");
        for(int i=0; i<100; i++){
            i+=1;   
            System.out.println(i);
            }
        
    }
    
}
